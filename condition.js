// if else condition

var price = 700;

var budget = 400;

if(price == budget){
    console.log('Buy');
}

else{
    console.log('Go Home');
}


// Multiple condition

var productPrice = 70000;

var salary = 60000;

var savings = 20000;


if(productPrice <= salary || (salary + savings) >= productPrice && savings >= 30000){
    console.log('I will buy the product');
}

else{
    console.log('Budget is not enough');
}


